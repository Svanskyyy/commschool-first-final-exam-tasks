# 1. შექმნილი სია 10 წიგნის
library = [
    {"title": "ორიენტ ექსპრესის მკვლელობა", "author": "აგათა კრისტი", "year": 1934},
    {"title": "ბასკერვილების ძაღლი", "author": "არტურ კონან დოილი", "year": 1902},
    {"title": "თოვლის კაცი", "author": "ჯო ნესბო", "year": 2007},
    {"title": "წითელი დრაკონი", "author": "თომას ჰარისი", "year": 1981},
    {"title": "გოგონა დრაკონის ტატუთი", "author": "სტიგ ლარსონი", "year": 2005},
    {"title": "როჯერ ეკროიდის მკვლელობა", "author": "აგათა კრისტი", "year": 1926},
    {"title": "წითელი ძაფი", "author": "არტურ კონან დოილი", "year": 1887},
    {"title": "მკვლელობა მინდორზე", "author": "აგათა კრისტი", "year": 1930},
    {"title": "დაიკარგა გოგონა", "author": "ჯილიან ფლინი", "year": 2012},
    {"title": "შავი ორქიდეა", "author": "ჯეიმს ელროი", "year": 1987}
]

# მთავარი ციკლი
while True:
    print("\n" + "=" * 40)
    print("დეტექტივური ჟანრის წიგნების მინი-ბიბლიოთეკა")
    print("=" * 40)
    print("1. ბიბლიოთეკის წიგნების ნახვა")
    print("2. ახალი წიგნის დამატება")
    print("3. წიგნის გატანა წასაკითხად (ნომრით)")
    print("4. პროგრამიდან გამოსვლა")
    print("=" * 40)

    choice = input("აირჩიეთ სასურველი მოქმედება (1-4): ")

    if choice == "1":
        # წიგნების სრული სიის ჩვენება
        print(f"\n--- ბიბლიოთეკაში არსებული წიგნები (სულ: {len(library)}) ---")
        # enumerate გვეხმარება სიის 1-დან დანომრვაში
        for index, book in enumerate(library, 1):
            print(f"{index}. \"{book['title']}\" - {book['author']} ({book['year']})")

    elif choice == "2":
        # ახალი წიგნის დამატება
        print("\n--- ახალი წიგნის დამატება ---")
        title = input("შეიყვანეთ წიგნის სათაური: ")
        author = input("შეიყვანეთ ავტორი: ")
        year = int(input("შეიყვანეთ გამოცემის წელი: "))

        # ახალი წექსიკონის შექმნა და სიაში დამატება (.append)
        new_book = {"title": title, "author": author, "year": year}
        library.append(new_book)

        print(f" წიგნი \"{title}\" წარმატებით დაემატა ბიბლიოთეკაში!")

    elif choice == "3":
        # წიგნის გატანა წასაკითხად ნომრის საშუალებით
        print(f"\n--- წიგნის გატანა წასაკითხად (სულ არის {len(library)} წიგნი) ---")

        # ჯერ ვუჩვენებთ სიას, რომ ნომერი მარტივად შეარჩიოს
        for index, book in enumerate(library, 1):
            print(f"{index}. \"{book['title']}\"")

        book_num = int(input("\nშეიყვანეთ იმ წიგნის ნომერი, რომლის გატანაც გსურთ: "))

        # რადგან py-ში ინდექსები იწყება 0-დან, ამიტომ მომხმარებლის შეყვანილ რიცხვს ვაკლებთ 1-ს
        selected_index = book_num - 1

        # წიგნის ამოღება და წაშლა სიიდან .pop() მეთოდით
        removed_book = library.pop(selected_index)

        print(
            f"თქვენ გაიტანეთ წიგნი: \"{removed_book['title']}\" - {removed_book['author']}. გისურვებთ სასიამოვნო კითხვას!")

    elif choice == "4":
        # ციკლიდან გამოსვლა და პროგრამის დასრულება
        print("\nმადლობა ბიბლიოთეკის სარგებლობისთვის!")
        break
    else:
        print("არასწორი არჩევანი! გთხოვთ აირჩიოთ 1-დან 4-მდე.")